/**
 * Busca una buseta por número exacto
 */
export function buscarPorNumero(numero) {
  return BUSETAS.find(b => b.numero === String(numero).trim());
}

/**
 * Filtra busetas cuyo número O placa contienen el texto buscado
 * @param {string} texto - lo que el usuario escribe
 * @param {number} limite - máximo de resultados
 */
export function filtrarBusetas(texto, limite = 8) {
  if (!texto || !texto.trim()) return [];
  const q = texto.trim().toLowerCase();
  return BUSETAS
    .filter(b =>
      b.numero.toLowerCase().startsWith(q) ||
      b.numero.toLowerCase().includes(q) ||
      b.placa.toLowerCase().includes(q)
    )
    .sort((a, b) => {
      // Priorizar los que empiezan con el texto
      const aStarts = a.numero.toLowerCase().startsWith(q);
      const bStarts = b.numero.toLowerCase().startsWith(q);
      if (aStarts && !bStarts) return -1;
      if (!aStarts && bStarts) return 1;
      return Number(a.numero) - Number(b.numero);
    })
    .slice(0, limite);
}
